<?php

namespace App\Console\Commands;

use Illuminate\Console\Command;
use App\Models\SolarPlant;
use App\Models\StorageSetting;
use App\Services\DocumentUploadConfig;
use App\Models\Document;

class TestSolarPlantDocumentsIntegration extends Command
{
    /**
     * The name and signature of the console command.
     *
     * @var string
     */
    protected $signature = 'test:solar-plant-documents-integration';

    /**
     * The console command description.
     *
     * @var string
     */
    protected $description = 'Testet die vollst√§ndige Integration des SolarPlant-Dokumentenmoduls';

    /**
     * Execute the console command.
     */
    public function handle()
    {
        $this->info('=== SOLARPLANT-DOKUMENTENMODUL INTEGRATIONSTEST ===');
        $this->newLine();

        try {
            // 1. StorageSetting pr√ºfen
            $this->info('1. StorageSetting-Konfiguration pr√ºfen...');
            $storageSetting = StorageSetting::current();
            
            if (!$storageSetting) {
                $this->error('‚ùå Keine StorageSetting gefunden');
                return 1;
            }

            $paths = $storageSetting->getStoragePaths();
            if (!isset($paths['solar_plants'])) {
                $this->error('‚ùå SolarPlant-Pfad-Konfiguration fehlt in StorageSetting');
                return 1;
            }

            $this->info('‚úÖ SolarPlant-Konfiguration gefunden:');
            $this->line('   Pattern: ' . $paths['solar_plants']['pattern']);
            $this->line('   Beispiel: ' . $paths['solar_plants']['example']);
            $this->newLine();

            // 2. SolarPlant-Model pr√ºfen
            $this->info('2. SolarPlant-Model und Beziehungen pr√ºfen...');
            
            $solarPlant = SolarPlant::first();
            if (!$solarPlant) {
                $this->info('   Keine SolarPlant gefunden, erstelle Test-SolarPlant...');
                $solarPlant = new SolarPlant();
                $solarPlant->name = 'Test Solaranlage Integration';
                $solarPlant->location = 'Teststra√üe 1, 12345 Teststadt';
                $solarPlant->total_capacity_kw = 25.5;
                $solarPlant->status = 'active';
                $solarPlant->save();
                $this->info('   ‚úÖ Test-SolarPlant erstellt: ' . $solarPlant->name);
            } else {
                $this->info('   ‚úÖ SolarPlant gefunden: ' . $solarPlant->name);
            }

            // Plant Number pr√ºfen
            if (!$solarPlant->plant_number) {
                $this->warn('   ‚ö†Ô∏è Plant Number fehlt, wird automatisch generiert...');
                $solarPlant->save(); // Trigger automatische Generierung
                $solarPlant->refresh();
            }
            $this->line('   Plant Number: ' . $solarPlant->plant_number);

            // Documents-Beziehung pr√ºfen
            if (!method_exists($solarPlant, 'documents')) {
                $this->error('‚ùå documents() Beziehung fehlt im SolarPlant Model');
                return 1;
            }
            $this->info('   ‚úÖ documents() Beziehung verf√ºgbar');
            $this->newLine();

            // 3. DocumentUploadConfig testen
            $this->info('3. DocumentUploadConfig::forSolarPlants() testen...');
            
            try {
                $config = DocumentUploadConfig::forSolarPlants($solarPlant);
                $this->info('   ‚úÖ DocumentUploadConfig erstellt');
                $this->line('   Storage Directory: ' . $config->getStorageDirectory());
                $this->line('   Kategorien: ' . implode(', ', array_keys($config->get('categories'))));
                $this->line('   Zeitstempel aktiviert: ' . ($config->get('timestampFilenames') ? 'Ja' : 'Nein'));
            } catch (\Exception $e) {
                $this->error('‚ùå Fehler bei DocumentUploadConfig: ' . $e->getMessage());
                return 1;
            }
            $this->newLine();

            // 4. Pfad-Aufl√∂sung testen
            $this->info('4. Pfad-Aufl√∂sung testen...');
            
            $resolvedPath = $storageSetting->resolvePath('solar_plants', $solarPlant);
            $configPath = $config->getStorageDirectory();
            
            $this->line('   StorageSetting resolved: ' . $resolvedPath);
            $this->line('   DocumentUploadConfig:    ' . $configPath);
            
            if ($resolvedPath === $configPath) {
                $this->info('   ‚úÖ Pfade stimmen √ºberein');
            } else {
                $this->error('   ‚ùå Pfade stimmen nicht √ºberein');
                return 1;
            }
            $this->newLine();

            // 5. Kategorien und Farben testen
            $this->info('5. Solaranlagen-spezifische Kategorien testen...');
            
            $categories = $config->get('categories');
            $expectedCategories = [
                'planning', 'permits', 'installation', 'commissioning',
                'maintenance', 'monitoring', 'insurance', 'technical',
                'financial', 'legal', 'other'
            ];
            
            foreach ($expectedCategories as $category) {
                if (isset($categories[$category])) {
                    $this->line('   ‚úÖ ' . $category . ': ' . $categories[$category]);
                } else {
                    $this->error('   ‚ùå Kategorie fehlt: ' . $category);
                    return 1;
                }
            }
            $this->newLine();

            // 6. Platzhalter-System testen
            $this->info('6. Platzhalter-System testen...');
            
            $placeholders = $storageSetting->getAvailablePlaceholders('solar_plants');
            $expectedPlaceholders = [
                'plant_number', 'plant_name', 'plant_id',
                'solar_plant_number', 'solar_plant_name', 'solar_plant_id'
            ];
            
            foreach ($expectedPlaceholders as $placeholder) {
                if (isset($placeholders[$placeholder])) {
                    $this->line('   ‚úÖ {' . $placeholder . '}: ' . $placeholders[$placeholder]);
                } else {
                    $this->error('   ‚ùå Platzhalter fehlt: {' . $placeholder . '}');
                    return 1;
                }
            }
            $this->newLine();

            // 7. Filament RelationManager pr√ºfen
            $this->info('7. Filament RelationManager pr√ºfen...');
            
            $relationManagerClass = 'App\\Filament\\Resources\\SolarPlantResource\\RelationManagers\\DocumentsRelationManager';
            if (class_exists($relationManagerClass)) {
                $this->info('   ‚úÖ DocumentsRelationManager existiert');
                
                // SolarPlantResource Relations pr√ºfen
                $solarPlantResourceClass = 'App\\Filament\\Resources\\SolarPlantResource';
                if (class_exists($solarPlantResourceClass)) {
                    $relations = $solarPlantResourceClass::getRelations();
                    if (in_array($relationManagerClass, $relations)) {
                        $this->info('   ‚úÖ DocumentsRelationManager in SolarPlantResource registriert');
                    } else {
                        $this->error('   ‚ùå DocumentsRelationManager nicht in SolarPlantResource registriert');
                        return 1;
                    }
                } else {
                    $this->error('   ‚ùå SolarPlantResource nicht gefunden');
                    return 1;
                }
            } else {
                $this->error('   ‚ùå DocumentsRelationManager nicht gefunden');
                return 1;
            }
            $this->newLine();

            // 8. Finale Struktur-√úbersicht
            $this->info('8. Finale Dokumentenstruktur:');
            $this->line('');
            $this->line('   DigitalOcean Space: jtsolarbau/');
            $this->line('   ‚îú‚îÄ‚îÄ suppliers-documents/ (alte Supplier-Dateien)');
            $this->line('   ‚îî‚îÄ‚îÄ documents/');
            $this->line('       ‚îú‚îÄ‚îÄ suppliers/');
            $this->line('       ‚îÇ   ‚îî‚îÄ‚îÄ LF-XXX-Supplier-Name/ (Supplier-Dokumente)');
            $this->line('       ‚îú‚îÄ‚îÄ clients/');
            $this->line('       ‚îÇ   ‚îî‚îÄ‚îÄ KD-XXX-Customer-Name/ (Client-Dokumente)');
            $this->line('       ‚îî‚îÄ‚îÄ solar_plants/');
            $this->line('           ‚îî‚îÄ‚îÄ ' . $solarPlant->plant_number . '-' . str_replace(' ', '-', $solarPlant->name) . '/ (SolarPlant-Dokumente)');
            $this->line('               ‚îú‚îÄ‚îÄ planung_2025-01-07_12-33-21.pdf');
            $this->line('               ‚îú‚îÄ‚îÄ genehmigung_2025-01-07_12-33-21.pdf');
            $this->line('               ‚îú‚îÄ‚îÄ installation_2025-01-07_12-33-21.pdf');
            $this->line('               ‚îú‚îÄ‚îÄ inbetriebnahme_2025-01-07_12-33-21.pdf');
            $this->line('               ‚îî‚îÄ‚îÄ wartung_2025-01-07_12-33-21.pdf');
            $this->newLine();

            // 9. Zusammenfassung
            $this->info('=== ZUSAMMENFASSUNG ===');
            $this->info('‚úÖ SolarPlant-Dokumentenmodul vollst√§ndig implementiert');
            $this->info('‚úÖ StorageSetting um SolarPlant-Konfiguration erweitert');
            $this->info('‚úÖ DocumentUploadConfig::forSolarPlants() funktioniert');
            $this->info('‚úÖ Solaranlagen-spezifische Kategorien verf√ºgbar');
            $this->info('‚úÖ Platzhalter-System f√ºr SolarPlants implementiert');
            $this->info('‚úÖ Filament DocumentsRelationManager erstellt und registriert');
            $this->info('‚úÖ Automatische Zeitstempel-Generierung aktiviert');
            $this->info('‚úÖ Strukturierte Pfade: documents/solar_plants/{plant_number}-{plant_name}/');
            $this->newLine();

            $this->info('üéâ SolarPlant-Dokumentenmodul erfolgreich getestet!');
            $this->info('Das Modul ist bereit f√ºr den produktiven Einsatz.');

            return 0;

        } catch (\Exception $e) {
            $this->error('‚ùå Unerwarteter Fehler: ' . $e->getMessage());
            $this->error('Datei: ' . $e->getFile() . ':' . $e->getLine());
            return 1;
        }
    }
}